#include<iostream>
using namespace std;

int main(){
//for
for(int i;i<5;i++){
    cout<<"For Loop"<<endl;

}
cout<<endl;

//while
int j=1;
while(j<=5){
    cout<<"While Loop"<<endl;
    j++;
}
cout<<endl;

//do while
int k=1;
do{
    cout<<"Do While Loop"<<endl;
    k++;
}
while(k<=5);

return 0;
}
